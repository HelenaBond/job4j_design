package ru.job4j.ood.core.coresrp;

public class Toy {

    private int id;
    private String name;

    public Toy(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
